public class Shape {
    void length(int r){
        System.out.println("length equals to: " + 2 * 3.14 * r);
    }
    void length(int r, int b){
        System.out.println("p equals to: " +  b * r);
    }
    void length(int r , int b, int c){
        System.out.println("pt equals to: "+ (c + b + r));
    }
}
